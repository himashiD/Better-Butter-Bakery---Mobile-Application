package com.example.BetterButter;

public class Weddingcakesclass {
    String wid = "";
    String wcakename = "";
    String wcakeid = "";
    String wcakeweight = "";
    String wcakecolors = "";
    String wcakenote = "";
    String wcakeprice = "";
    String wcakeqty = "";
    String wcreated_at = "";


    public Weddingcakesclass(String wid, String wcakename, String wcakeid, String wcakeweight, String wcakecolors, String wcakenote, String wcakeprice, String wcakeqty, String wcreated_at) {
        this.wid = wid;
        this.wcakename = wcakename;
        this.wcakeid = wcakeid;
        this.wcakeweight = wcakeweight;
        this.wcakecolors = wcakecolors;
        this.wcakenote = wcakenote;
        this.wcakeprice = wcakeprice;
        this.wcakeqty = wcakeqty;
        this.wcreated_at = wcreated_at;
    }

    public Weddingcakesclass() {
    }

    public String getWid() {
        return wid;
    }

    public void setWid(String wid) {
        this.wid = wid;
    }

    public String getWcakename() {
        return wcakename;
    }

    public void setWcakename(String wcakename) {
        this.wcakename = wcakename;
    }

    public String getWcakeid() {
        return wcakeid;
    }

    public void setWcakeid(String wcakeid) {
        this.wcakeid = wcakeid;
    }

    public String getWcakeweight() {
        return wcakeweight;
    }

    public void setWcakeweight(String wcakeweight) {
        this.wcakeweight = wcakeweight;
    }

    public String getWcakecolors() {
        return wcakecolors;
    }

    public void setWcakecolors(String wcakecolors) {
        this.wcakecolors = wcakecolors;
    }

    public String getWcakenote() {
        return wcakenote;
    }

    public void setWcakenote(String wcakenote) {
        this.wcakenote = wcakenote;
    }

    public String getWcakeprice() {
        return wcakeprice;
    }

    public void setWcakeprice(String wcakeprice) {
        this.wcakeprice = wcakeprice;
    }

    public String getWcakeqty() {
        return wcakeqty;
    }

    public void setWcakeqty(String wcakeqty) {
        this.wcakeqty = wcakeqty;
    }

    public String getWcreated_at() {
        return wcreated_at;
    }

    public void setWcreated_at(String wcreated_at) {
        this.wcreated_at = wcreated_at;
    }
}
